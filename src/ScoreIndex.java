
public class ScoreIndex {
	public int index;
	public int score;
	
	public ScoreIndex(int score, int index) {
		this.score = score;
		this.index = index;
	}
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
}
